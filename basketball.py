import turtle

turtle.Screen().bgcolor('light steel blue')
t = turtle.Turtle()
t.speed(10)
t.pensize(5)

for i in range(4):
    t.forward(100)
    t.left(90)
    t.up()

t.goto(50, 0)
t.down()
t.up()
t.fillcolor('orange')
t.begin_fill()

t.circle(50)
t.end_fill()
t.down()
t.up()
t.goto(0, 50)
t.down()
t.forward(100)
t.up()
t.goto(50, 100)
t.down()
t.right(90)
t.forward(100)
t.up()
t.left(90)
t.circle(50, 135)
t.down()

t.left(90)
t.circle(50, 90)
t.up()
t.left(90)
t.circle(50, 270)
t.down()
t.left(90)
t.circle(50, 90)
t.hideturtle()
